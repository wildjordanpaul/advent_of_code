    override fun solveProblem1(input: String): String {
        val cases = input.split("\n").map { line ->
            val numbers = line.split(" ").map(String::toInt)
            D16Case(
                numbers.take(4),
                numbers.subList(4,8),
                numbers.takeLast(4)
            )
        }.filter { case ->
            val matches = Instruction.values().filter { instruction ->
                instruction.validate(case)
            }
            val current = possibles[case.instruction[0]]!!
            possibles[case.instruction[0]] = matches.intersect(current).toList()

            matches.size >= 3
        }

        return cases.size.toString()
    }

    override fun solveProblem2(input: String): String {
        var changed = false
        do {
            changed = false
            possibles.filter { it.value.size == 1 }.forEach { (entry, possible) ->
                possibles.forEach { e, v ->
                    if (e != entry && v.contains(possible.first())) {
                        changed = true
                        possibles[e] = (v - possible.first())
                    }
                }
            }
        } while(changed)

        var register = listOf(0,0,0,0)

        input
            .split("\n")
            .map { line -> line.split(" ").map(String::toInt) }
            .forEach { code ->
                register = possibles[code[0]]!!.first().execute(register, code)
            }

        return register[0].toString()
    }

